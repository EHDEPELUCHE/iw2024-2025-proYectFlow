package es.uca.iw.global;

/**
 * Enumeración que representa los diferentes roles disponibles en el sistema.
 * 
 * <ul>
 *   <li><b>SOLICITANTE</b>: Usuario que realiza una solicitud.</li>
 *   <li><b>PROMOTOR</b>: Usuario que promueve o gestiona proyectos.</li>
 *   <li><b>CIO</b>: Director de Información (Chief Information Officer).</li>
 *   <li><b>OTP</b>: Otro tipo de personal.</li>
 *   <li><b>ADMIN</b>: Administrador del sistema con permisos completos.</li>
 * </ul>
 */
public enum Roles {SOLICITANTE, PROMOTOR, CIO, OTP, ADMIN}